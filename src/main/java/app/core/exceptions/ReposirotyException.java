package app.core.exceptions;

public class ReposirotyException extends CouponSystemException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ReposirotyException() {
		super();
	}

	public ReposirotyException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ReposirotyException(String message, Throwable cause) {
		super(message, cause);
	}

	public ReposirotyException(String message) {
		super(message);
	}

	public ReposirotyException(Throwable cause) {
		super(cause);
	}

}
